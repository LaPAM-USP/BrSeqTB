# ================== FUNCTIONS ==================
def build_db(brseq_list):
    db = {}
    for rec in brseq_list:
        pos = int(rec["Position"])
        alt = str(rec["Alternative allele"]).upper()
        db[(pos, alt)] = (
            rec.get("Lineage", ""),
            rec.get("Lineage details", ""),
            rec.get("Comment", ""),
        )
    return db

  
def _opener(path):
    return gzip.open(path, "rt") if path.endswith(".gz") else open(path)


def annotate_vcf(vcf_path, out_csv, db_map):

    with _opener(vcf_path) as fin, open(out_csv, "w", newline="") as fout:

        writer = csv.writer(fout)
        header_written = False

        for line in fin:

            if line.startswith("##"):
                continue

            if line.startswith("#CHROM"):
                cols = line.strip().lstrip("#").split("\t")
                writer.writerow(cols + ["LINEAGE", "LINEAGE_DETAILS", "COMMENT"])
                header_written = True
                continue

            parts = line.rstrip("\n").split("\t")

            if len(parts) < 8:
                if not header_written:
                    writer.writerow([
                        "CHROM","POS","REF","ALT","QUAL","FILTER","INFO",
                        "LINEAGE","LINEAGE_DETAILS","COMMENT"
                    ])
                    header_written = True

                writer.writerow(parts + ["","",""])
                continue

            chrom, pos, vid, ref, alt, qual, filt, info = parts[:8]
            pos_i = int(pos)

            lin_values = []
            det_values = []
            com_values = []

            for a in alt.split(","):
                key = (pos_i, a.upper())
                lin, det, com = db_map.get(key, ("","",""))
                lin_values.append(lin)
                det_values.append(det)
                com_values.append(com)

            writer.writerow(parts + [
                ",".join(lin_values),
                ",".join(det_values),
                ",".join(com_values)
            ])


# ================== MAIN ==================
def main():

    if len(sys.argv) != 2:
        print("Usage: python3 lineage.py <biosample>")
        sys.exit(1)

    biosample = sys.argv[1]

    # ---------- INPUT ----------
    vcf_path = os.path.join(
        NORM_DIR,
        biosample,
        f"{biosample}_norm.vcf.gz"
    )

    if not os.path.exists(vcf_path):
        print(f"[ERROR] Normalized VCF not found:\n  {vcf_path}")
        sys.exit(1)

    # ---------- OUTPUT ----------
    out_folder = os.path.join(OUT_DIR, biosample)
    os.makedirs(out_folder, exist_ok=True)

    out_file = os.path.join(
        out_folder,
        f"{biosample}_lineage_summary.csv"
    )

    # ---------- SKIP ----------
    if os.path.exists(out_file):
        print(f"[SKIP] Lineage summary already exists:\n  {out_file}")
        sys.exit(0)

    print(f"[RUN] Lineage annotation for: {biosample}")
    print(f"[IN]  {vcf_path}")
    print(f"[OUT] {out_file}")

    db_map = build_db(BrSeq_db)
    annotate_vcf(vcf_path, out_file, db_map)

    print(f"[OK] Saved lineage summary:\n  {out_file}")
    print("[DONE]")


if __name__ == "__main__":
    main()

